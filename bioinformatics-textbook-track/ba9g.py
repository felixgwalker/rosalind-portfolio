# BA9G — Construct the Suffix Array of a String
# https://rosalind.info/problems/ba9g/
#
# Given: a DNA string s. Return: the suffix array (sorted order of all suffixes).

import os, sys

def get_input():
    p = os.path.join(os.path.dirname(os.path.abspath(__file__)), '..', 'rosalind-inputs', 'bioinformatics-textbook-track', 'rosalind_ba9g.txt')
    if os.path.exists(p):
        return open(p).read().strip(), p.replace('rosalind-inputs', 'rosalind-outputs')
    return sys.stdin.read().strip(), None

def solve(data):
    s = data.strip()
    # Build suffix array: sort indices by their suffix
    sa = sorted(range(len(s)), key=lambda i: s[i:])
    print(', '.join(map(str, sa)))

if __name__ == '__main__':
    import io, contextlib
    data, out_path = get_input()
    buf = io.StringIO()
    with contextlib.redirect_stdout(buf):
        solve(data)
    output = buf.getvalue()
    sys.stdout.write(output)
    if out_path:
        os.makedirs(os.path.dirname(out_path), exist_ok=True)
        with open(out_path, 'w') as f:
            f.write(output)
